public class CarSpecificationDetails {

    private String name;
    private String type;
    private int speed;

    public CarSpecificationDetails(String name, String type, int speed) {
        this.name = name;
        this.type = type;
        this.speed = speed;
    }

    public String getName() {
        return name;
    }

    public String getType() {
        return type;
    }

    public int getSpeed() {
        return speed;
    }
    
}
